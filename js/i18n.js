// Language detection and setting
function detectLanguage() {
  const urlParams = new URLSearchParams(window.location.search);
  const urlLang = urlParams.get('lang');
  
  if (urlLang && window.__i18nDict[urlLang]) {
    return urlLang;
  }
  
  const storedLang = localStorage.getItem('wegc_lang');
  if (storedLang && window.__i18nDict[storedLang]) {
    return storedLang;
  }
  
  return 'en'; // default
}

// Set language
function setLang(lang) {
  if (!window.__i18nDict[lang]) return;
  
  const dict = window.__i18nDict[lang];
  
  // Update meta tags
  document.querySelectorAll('meta[data-i18n]').forEach(meta => {
    const key = meta.getAttribute('data-i18n');
    if (dict[key]) {
      meta.setAttribute('content', dict[key]);
    }
  });
  
  // Update title
  const title = document.querySelector('title[data-i18n]');
  if (title && dict[title.getAttribute('data-i18n')]) {
    title.textContent = dict[title.getAttribute('data-i18n')];
  }
  
  // Update text content
  document.querySelectorAll('[data-i18n]').forEach(el => {
    const key = el.getAttribute('data-i18n');
    if (dict[key]) {
      el.textContent = dict[key];
    }
  });
  
  // Update placeholders
  document.querySelectorAll('[data-i18n-placeholder]').forEach(el => {
    const key = el.getAttribute('data-i18n-placeholder');
    if (dict[key]) {
      el.setAttribute('placeholder', dict[key]);
    }
  });
  
  // Update internal links with lang parameter
  document.querySelectorAll('a[href^="/"]').forEach(link => {
    const href = link.getAttribute('href');
    if (href.includes('?')) {
      link.setAttribute('href', href.replace(/(\?.*)?$/, '?lang=' + lang));
    } else {
      link.setAttribute('href', href + '?lang=' + lang);
    }
  });
  
  // Store language preference
  localStorage.setItem('wegc_lang', lang);
  
  // Update URL without reload
  const url = new URL(window.location);
  url.searchParams.set('lang', lang);
  window.history.replaceState({}, '', url);
}

// Initialize on page load
document.addEventListener('DOMContentLoaded', function() {
  const currentLang = detectLanguage();
  setLang(currentLang);
  
  // Update language selector
  const langLabel = document.getElementById('langLabel');
  if (langLabel) {
    langLabel.textContent = currentLang.toUpperCase();
  }
});
